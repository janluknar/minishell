#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	unsigned char *ptr;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	ptr = malloc(size * count);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, count * sizeof(size));
	return ((void*)ptr);
}
